package ArrayYouTube;

public class ArrayYoutubeLesson2 {
    public static void main(String[] args) {
        // присваиваем значение массиву

        int mas[] = {22, 22, 98 ,89 ,66};
        for (int c : mas) { // Все элементы нашего массива будут поочередно попадать в перемееную "С"
            System.out.println(c);
        }
    }

}
