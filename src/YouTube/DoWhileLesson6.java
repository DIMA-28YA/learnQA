package YouTube;

public class DoWhileLesson6 {
    public static void main(String[] args) {


        // Таблица умножения на одно число

        int i = 1;
        int x = 3;

        do {
            System.out.printf("%s * %s = %s \n", i, x, i * x);
            i++;
        }
        while (i <= 9); // только условие (условие)
    }
}
